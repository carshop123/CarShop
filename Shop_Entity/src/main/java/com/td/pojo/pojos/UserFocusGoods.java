package com.td.pojo.pojos;


public class UserFocusGoods {

  private Integer id;
  private Integer uid;
  private Integer focusGoodsId;
  private String focusGoodsAddr;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public Integer getUid() {
    return uid;
  }

  public void setUid(Integer uid) {
    this.uid = uid;
  }


  public Integer getFocusGoodsId() {
    return focusGoodsId;
  }

  public void setFocusGoodsId(Integer focusGoodsId) {
    this.focusGoodsId = focusGoodsId;
  }


  public String getFocusGoodsAddr() {
    return focusGoodsAddr;
  }

  public void setFocusGoodsAddr(String focusGoodsAddr) {
    this.focusGoodsAddr = focusGoodsAddr;
  }

}
