package com.td.pojo.pojos;


public class UserFocusShop {

  private Integer id;
  private Integer uid;
  private Integer focusShopId;
  private String focusShopAddr;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public Integer getUid() {
    return uid;
  }

  public void setUid(Integer uid) {
    this.uid = uid;
  }


  public Integer getFocusShopId() {
    return focusShopId;
  }

  public void setFocusShopId(Integer focusShopId) {
    this.focusShopId = focusShopId;
  }


  public String getFocusShopAddr() {
    return focusShopAddr;
  }

  public void setFocusShopAddr(String focusShopAddr) {
    this.focusShopAddr = focusShopAddr;
  }

}
